// 1 - Define a variable named counter (number)
// 2 - Write a while loop that:
//      - counts down
//      - outputs the counter value in the terminal like so:
//       "3"
//       "2"
//       "1"
//       "Go!"

var counter = 3;
while (counter > 0) {
  console.log(counter);
  counter--;
}
console.log("Go!");
